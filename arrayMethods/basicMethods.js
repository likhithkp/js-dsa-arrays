// Create and initialize an array
// An array can hold on multiple elements or any js element type

let person = {
    name: "likhith",
    age: 35,
    profession: "Spiderman"
};

let fruits = ['Apple', 'Mango', person];

/** Accessing array element
    Pass the index into the square brackets next to array name
    Index in array always start at 0
*/
fruits[0]; 

// .length method is used to count size of an array
fruits.length;
